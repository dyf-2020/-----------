#include"boss.h"


//显示职工个人信息
void Boss::showInfo() {
	cout << "职工编号：" << this->m_Id
		<< "\t职工姓名：" << this->m_Name
		<< "\t所在部门：" << getDeptName()
		<< "\t岗位职责：" << "管理公司所有事务" << endl;
}
//获取岗位信息
string Boss::getDeptName() {
	return string("老板");
}
//构造函数
Boss::Boss(int id, string name, int dId) {
	this->m_Id = id;
	this->m_Name = name;
	this->m_DeptId = dId;
}