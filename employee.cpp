#include"employee.h"


//显示职工个人信息
void Employee::showInfo() {
	cout << "职工编号：" << this->m_Id
		<< "\t职工姓名：" << this->m_Name
		<< "\t所在部门：" << getDeptName()
		<< "\t岗位职责：" << "完成经理交给的任务" << endl;
}
//获取岗位信息
string Employee::getDeptName() {
	return string("员工");
}
//构造函数
Employee::Employee(int id, string name, int dId) {
	this->m_Id = id;
	this->m_Name = name;
	this->m_DeptId = dId;
}